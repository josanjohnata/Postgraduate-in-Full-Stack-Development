// Programação sequencial segue a sequencia de cascata, logo o programa é lido linha por linha na ordem de cima para baixo e da direita para esquerda.

class Main {
  public static void main(String[] args) {
    double base, height, area;

    base = 10.0;
    height = 8.0;

    area = (base * height) / 2;
    System.out.println(area);
  }
}
